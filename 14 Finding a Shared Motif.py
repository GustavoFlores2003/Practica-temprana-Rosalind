def leer_fasta(fasta_texto):
    """ Función para leer secuencias en formato FASTA """
    secuencias = {}
    secuencia_actual = ""
    identificador = None

    for linea in fasta_texto.splitlines():
        if linea.startswith(">"):  # Identificador de secuencia
            identificador = linea[1:].strip()
            secuencias[identificador] = ""
        else:
            secuencias[identificador] += linea.strip()
    
    return list(secuencias.values())

def generar_subcadenas(secuencia):
    """ Genera todas las subcadenas posibles de una secuencia """
    longitud = len(secuencia)
    subcadenas = set()
    
    for i in range(longitud):
        for j in range(i + 1, longitud + 1):
            subcadenas.add(secuencia[i:j])
    
    return sorted(subcadenas, key=len, reverse=True)  # Ordenar de mayor a menor

def encontrar_motivo_comun_largo(secuencias):
    """ Encuentra la subcadena común más larga entre todas las secuencias """
    secuencias.sort(key=len)  # Ordenar para usar la más corta como referencia
    base = secuencias[0]  # Tomar la más corta
    
    # Generar todas las subcadenas de la más corta
    subcadenas_posibles = generar_subcadenas(base)
    
    # Buscar la subcadena más larga que esté en todas las secuencias
    for subcadena in subcadenas_posibles:
        if all(subcadena in sec for sec in secuencias):
            return subcadena  # Retornamos la primera que cumple (es la más larga)

    return ""  # Si no encontramos ninguna, devolvemos cadena vacía

